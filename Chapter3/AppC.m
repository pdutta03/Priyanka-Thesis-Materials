function [ c33, c44, eps, gam, del, eta ] = AppC( c, ar )

% Approximate Voigt summation using Legendre polynomials

e = (c(1,1)-c(3,3))/(2*c(3,3));
g = (c(6,6)-c(4,4))/(2*c(4,4));
d = ((c(1,3)+c(4,4))^2 - (c(3,3)-c(4,4))^2)/(2*c(3,3)*(c(3,3)-c(4,4)));

for n = 1:length(ar)
    ciso = zeros(6,6);
    
    ciso(1,1) = (1/15)*(8*c(1,1)+3*c(3,3)+4*c(1,3)+8*c(4,4));
    ciso(2,2) = ciso(1,1); ciso(3,3) = ciso(1,1);
    
    ciso(1,2) = (1/15)*(c(1,1)+c(3,3)+5*c(1,2)+8*c(1,3)-4*c(4,4));
    ciso(2,3) = ciso(1,2); ciso(3,1) = ciso(1,2);
    ciso(2,1) = ciso(1,2);ciso(1,3) = ciso(3,1);ciso(3,2) = ciso(2,3);
    
    ciso(4,4) = (1/30)*(7*c(1,1)+2*c(3,3)-5*c(1,2)-4*c(1,3)+12*c(4,4));
    ciso(5,5) = ciso(4,4); ciso(6,6) = ciso(4,4);
    
    
    
    c1 = c(1,1)+c(3,3)-2*c(1,3)-4*c(4,4);
    c2 = c(1,1)-3*c(1,2)+2*c(1,3)-2*c(4,4);
    c3 = 4*c(1,1)-3*c(3,3)-c(1,3)-2*c(4,4);
    
    
    
    delc = zeros(6,6); a = ar(n);
    
    delc(1,1) = (8*sqrt(10)/105)*(pi.^2)*c3*(w(2,0,0,a) - sqrt(6)*w(2,2,0,a)) + (4*sqrt(2)/35)*(pi.^2)*c1*(w(4,0,0,a) - (2*sqrt(10)/3)*w(4,2,0,a) + (sqrt(70)/3)*w(4,4,0,a)); 
    
    delc(2,2) = (8*sqrt(10)/105)*(pi.^2)*c3*(w(2,0,0,a) + sqrt(6)*w(2,2,0,a)) + (4*sqrt(2)/35)*(pi.^2)*c1*(w(4,0,0,a) + (2*sqrt(10)/3)*w(4,2,0,a) + (sqrt(70)/3)*w(4,4,0,a)); 
    
    delc(3,3) = (16*sqrt(2)/105)*(pi.^2)*(2*c1*w(4,0,0,a) - sqrt(5)*c3*w(2,0,0,a));
    
    delc(1,2) = -(8*sqrt(10)/315)*(pi.^2)*(7*c2 - c3)*w(2,0,0,a) + (4*sqrt(2)/105)*(pi.^2)*c1*(w(4,0,0,a) - sqrt(70)*w(4,4,0,a)); delc(2,1) = delc(1,2);
    
    delc(1,3) = (4*sqrt(10)/315)*(pi.^2)*(7*c2 - c3)*(w(2,0,0,a) + sqrt(6)*w(2,2,0,a)) - (16*sqrt(2)/105)*(pi.^2)*c1*(w(4,0,0,a) - sqrt(5/2)*w(4,2,0,a)); delc(3,1) = delc(1,3);
    
    delc(2,3) = (4*sqrt(10)/315)*(pi.^2)*(7*c2 - c3)*(w(2,0,0,a) - sqrt(6)*w(2,2,0,a)) - (16*sqrt(2)/105)*(pi.^2)*c1*(w(4,0,0,a) + sqrt(5/2)*w(4,2,0,a)); delc(3,2) = delc(2,3);
    
    delc(4,4) = -(2*sqrt(10)/315)*(pi.^2)*(7*c2 + 2*c3)*(w(2,0,0,a) - sqrt(6)*w(2,2,0,a)) - (16*sqrt(2)/105)*(pi.^2)*c1*(w(4,0,0,a) + sqrt(5/2)*w(4,2,0,a));
    
    delc(5,5) = -(2*sqrt(10)/315)*(pi.^2)*(7*c2 + 2*c3)*(w(2,0,0,a) + sqrt(6)*w(2,2,0,a)) - (16*sqrt(2)/105)*(pi.^2)*c1*(w(4,0,0,a) - sqrt(5/2)*w(4,2,0,a));
    
    delc(6,6) = (4*sqrt(10)/315)*(pi.^2)*(7*c2 + 2*c3)*w(2,0,0,a) + (4*sqrt(2)/105)*(pi.^2)*c1*(w(4,0,0,a) - sqrt(70)*w(4,4,0,a));
    
    cvti = ciso + delc;
    
    c33(n) = cvti(3,3);
    c44(n) = cvti(4,4);
    eps(n) = (cvti(1,1)-cvti(3,3))/(2*cvti(3,3));
    gam(n) = (cvti(6,6)-cvti(4,4))/(2*cvti(4,4));
    del(n) = ((cvti(1,3)+cvti(4,4))^2 - (cvti(3,3)-cvti(4,4))^2)/(2*cvti(3,3)*(cvti(3,3)-cvti(4,4)));
    eta(n) = (eps(n) - del(n))./(1+2*del(n));
end

end

